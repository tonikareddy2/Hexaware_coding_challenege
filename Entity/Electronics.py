class Electronics:
    def __init__(
        self,
        productId,
        productName,
        description,
        price,
        quantityInStock,
        type,
        brand,
        warrantyPeriod,
    ):
        self.productId = productId
        self.productName = productName
        self.description = description
        self.price = price
        self.quantityInStock = quantityInStock
        self.type = type
        self.brand = brand
        self.warrantyPeriod = warrantyPeriod
